package com.revature.rideshare.user.beans;

public class ContactType {
	
	private int id;
	private String type;
	
	public ContactType() {
		super();
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	

}
